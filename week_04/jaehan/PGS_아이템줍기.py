## 아이템 줍기

## rectangle(2차원배열)에서 characterX ,characterY 위치에서 itemX, itemY 까지의 최단 거리
from collections import deque

def solution(rectangle, characterX, characterY, itemX, itemY):
    # --------------------------------------------------
    # 1. 좌표를 2배로 늘리는 이유
    # --------------------------------------------------
    # 이 문제는 사각형의 테두리만 이동해야 한다.
    #
    # 그런데 원래 좌표 그대로 처리하면,
    # 대각선으로 맞닿은 부분이나 모서리 부분에서
    # 실제로는 갈 수 없는 길이 연결된 것처럼 보일 수 있다.
    #
    # 그래서 모든 좌표를 2배로 늘려서 중간 공간을 만들어준다.
    # 예: (1, 1) -> (2, 2)
    # --------------------------------------------------

    characterX *= 2
    characterY *= 2
    itemX *= 2
    itemY *= 2

    # --------------------------------------------------
    # 2. 전체 좌표판 생성
    # --------------------------------------------------
    # 문제에서 좌표는 최대 50까지 주어진다.
    # 좌표를 2배로 늘리면 최대 100까지 필요하다.
    #
    # 여유 있게 102 x 102 크기의 2차원 배열을 만든다.
    #
    # board 값의 의미:
    # 0 = 아무것도 아님 / 이동 불가
    # 1 = 테두리 / 이동 가능
    # 2 = 사각형 내부 / 이동 불가
    # --------------------------------------------------

    board = [[0] * 102 for _ in range(102)]

    # --------------------------------------------------
    # 3. 사각형들을 board에 표시
    # --------------------------------------------------
    # rectangle의 각 원소는 [좌하단 x, 좌하단 y, 우상단 x, 우상단 y]
    #
    # 예:
    # [1, 1, 4, 4]
    #
    # 좌표를 2배로 늘리면:
    # [2, 2, 8, 8]
    #
    # 이 범위 안을 전부 돌면서
    # 경계선이면 1, 내부면 2로 표시한다.
    # --------------------------------------------------

    for x1, y1, x2, y2 in rectangle:
        # 사각형 좌표도 모두 2배로 늘린다.
        x1 *= 2
        y1 *= 2
        x2 *= 2
        y2 *= 2

        # x1 ~ x2, y1 ~ y2 범위 전체를 확인한다.
        for x in range(x1, x2 + 1):
            for y in range(y1, y2 + 1):

                # --------------------------------------------------
                # 내부 좌표인 경우
                # --------------------------------------------------
                # x가 양 끝이 아니고,
                # y도 양 끝이 아니면 사각형 내부이다.
                #
                # 내부는 절대 이동하면 안 되므로 2로 표시한다.
                # --------------------------------------------------
                if x1 < x < x2 and y1 < y < y2:
                    board[x][y] = 2

                # --------------------------------------------------
                # 테두리 좌표인 경우
                # --------------------------------------------------
                # x == x1 또는 x == x2 또는 y == y1 또는 y == y2
                # 라면 사각형의 경계선이다.
                #
                # 단, 이미 2로 표시된 곳은 내부라는 뜻이다.
                # 여러 사각형이 겹칠 때,
                # 어떤 사각형의 테두리가 다른 사각형 내부에 들어갈 수 있다.
                #
                # 그런 경우에는 이동 가능하면 안 되므로
                # board[x][y] != 2일 때만 테두리 1로 표시한다.
                # --------------------------------------------------
                elif board[x][y] != 2:
                    board[x][y] = 1

    # --------------------------------------------------
    # 4. BFS 준비
    # --------------------------------------------------
    # 캐릭터 위치에서 아이템 위치까지
    # board 값이 1인 곳, 즉 테두리만 따라 이동한다.
    # --------------------------------------------------

    queue = deque()
    queue.append((characterX, characterY, 0))
    # 현재 x좌표, y좌표, 이동 거리

    visited = [[False] * 102 for _ in range(102)]
    visited[characterX][characterY] = True

    # 상, 하, 좌, 우 이동 방향
    dx = [-1, 1, 0, 0]
    dy = [0, 0, -1, 1]

    # --------------------------------------------------
    # 5. BFS 실행
    # --------------------------------------------------
    # BFS는 가까운 거리부터 차례대로 탐색한다.
    # 따라서 아이템 위치에 처음 도착했을 때의 거리가 최단 거리이다.
    # --------------------------------------------------

    while queue:
        x, y, distance = queue.popleft()

        # 아이템 위치에 도착한 경우
        if x == itemX and y == itemY:
            # 좌표를 2배로 늘렸으므로 거리도 2배로 계산된다.
            # 따라서 실제 정답은 distance // 2 이다.
            return distance // 2

        # 상하좌우 탐색
        for i in range(4):
            nx = x + dx[i]
            ny = y + dy[i]

            # 좌표 범위 확인
            if nx < 0 or nx >= 102 or ny < 0 or ny >= 102:
                continue

            # 이미 방문한 곳이면 이동하지 않음
            if visited[nx][ny]:
                continue

            # 테두리인 곳만 이동 가능
            if board[nx][ny] == 1:
                visited[nx][ny] = True
                queue.append((nx, ny, distance + 1))