def solution(g, s):
    # [구조화 1: 정렬]
    # 효율적인 매칭을 위해 아이들의 요구사항(g)과 과자 크기(s)를 오름차순 정렬함
    g.sort()
    s.sort()

    child_i = 0  # 만족한 아이의 인덱스(및 총 수)
    cookie_j = 0  # 현재 검토 중인 과자의 인덱스

    # [구조화 2: 두 포인터를 이용한 탐색]
    # 모든 아이를 확인하거나 과자가 소진될 때까지 반복함
    while child_i < len(g) and cookie_j < len(s):

        # [구조화 3: 만족 여부 확인]
        # 현재 과자 크기가 아이의 최소 요구치 이상인 경우를 판별함
        if s[cookie_j] >= g[child_i]:
            # 아이가 만족했으므로 다음 아이로 인덱스를 이동함
            child_i += 1

        # [구조화 4: 다음 자원으로 이동]
        # 매칭 성공 여부와 관계없이 현재 과자는 사용된 것으로 간주하여 다음 과자로 이동함
        cookie_j += 1

    # [구조화 5: 결과 반환]
    # 최종적으로 만족시킨 아이들의 총 인원을 반환함
    return child_i